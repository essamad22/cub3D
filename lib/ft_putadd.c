#include "libft.h"

int	ft_putadd(unsigned long nb)
{
	int	len;

	len = 0;
	if (nb == 0)
		return (write(1, "0x0", 3));
	len = write(1, "0x", 2);
	ft_puthex(nb, &len, 'x');
	return (len);
}
